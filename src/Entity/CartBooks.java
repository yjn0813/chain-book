package Entity;

public class CartBooks {
	private String bookid ;
	private String bookname ;
	private String bookpic  ;
	private String chainprice ;
	private String shoppingnum ;
	private String singlsum ;
	private String total;
	private String address;
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getBookid() {
		return bookid;
	}
	public void setBookid(String bookid) {
		this.bookid = bookid;
	}
	public String getBookname() {
		return bookname;
	}
	public void setBookname(String bookname) {
		this.bookname = bookname;
	}
	public String getBookpic() {
		return bookpic;
	}
	public void setBookpic(String bookpic) {
		this.bookpic = bookpic;
	}
	public String getChainprice() {
		return chainprice;
	}
	public void setChainprice(String chainprice) {
		this.chainprice = chainprice;
	}
	public String getShoppingnum() {
		return shoppingnum;
	}
	public void setShoppingnum(String shoppingnum) {
		this.shoppingnum = shoppingnum;
	}
	public String getSinglsum() {
		return singlsum;
	}
	public void setSinglsum(String singlsum) {
		this.singlsum = singlsum;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	
}
